package com.hoau.mhow.module.bse.api.shared.domain;

import java.util.Date;

import com.hoau.hbdp.framework.entity.BaseEntity;

/**
 * 新闻实体
 * @author：张爱萍
 * @create：2015年6月4日 下午4:47:19
 * @description：
 */
public class NewsEntity extends BaseEntity{
	/**
	 *
	 */
	private static final long serialVersionUID = 734965425784392375L;
	private int newsId;
	/**
	 * 短标题
	 */
	private String shortTitile;
	/**
	 * 创建时间
	 */
	private Date createdAt;
	/**
	 * 标题
	 */
	private String title;
	
	/**
	 * 内容
	 */
	private String content;
	/**
	 * 点击次数
	 */
	private int clickCount;
	
	/**
	 * 缩略图路径
	 */
	private String sltSrc;
	
	/**
	 * 过期时间
	 */
	private Date disableTime;
	
	/**
	 * 简述
	 */
	private String description;
	
	/**
	 * 类目ID
	 */
	private String categoryId;
	
	/**
	 * 类目名
	 */
	private String categoryName;
	
	/**
	 * 在新闻中的排序
	 */
	private Integer rowNum;

	
	public Integer getRowNum() {
		return rowNum;
	}

	public void setRowNum(Integer rowNum) {
		this.rowNum = rowNum;
	}

	public int getNewsId() {
		return newsId;
	}

	public void setNewsId(int newsId) {
		this.newsId = newsId;
	}

	public String getShortTitile() {
		return shortTitile;
	}

	public void setShortTitile(String shortTitile) {
		this.shortTitile = shortTitile;
	}

	public Date getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(Date createdAt) {
		this.createdAt = createdAt;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getClickCount() {
		return clickCount;
	}

	public void setClickCount(int clickCount) {
		this.clickCount = clickCount;
	}

	public String getSltSrc() {
		return sltSrc;
	}

	public void setSltSrc(String sltSrc) {
		this.sltSrc = sltSrc;
	}

	public Date getDisableTime() {
		return disableTime;
	}

	public void setDisableTime(Date disableTime) {
		this.disableTime = disableTime;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	
}

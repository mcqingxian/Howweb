package com.hoau.wechat.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * @author gaojia
 *
 */
public class UserEntity implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 8912333690415648816L;
	private String _id;
	private String openId;
	private String omsUserId;
	private String name;
	private String password;
	
	private Date createTime;
	public String getOmsUserId() {
		return omsUserId;
	}
	public void setOmsUserId(String omsUserId) {
		this.omsUserId = omsUserId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String get_id() {
		return _id;
	}
	public void set_id(String _id) {
		this._id = _id;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
}

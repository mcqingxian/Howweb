package com.hoau.wechat.vo;

import org.codehaus.jackson.annotate.JsonProperty;

/** 
* @ClassName  :TrackRecords 
* @Description:跟踪记录信息
* @author     :xujun cometzb@126.com	
* @date       :2014年4月25日 上午11:01:06 
*  
*/
public class TrackRecords {
	@JsonProperty("Status")
	private String status;
	@JsonProperty("Checkpoint")
	private String checkpoint;
	@JsonProperty("Desc")
	private String desc;
	@JsonProperty("TimeStamp")
	private String timeStamp;
	@JsonProperty("NextCheckpoint")
	private String nextCheckpoint;
	@JsonProperty("StatusCode")
	private String statusCode;
	@JsonProperty("CompanyName")
	private String companyName;
	@JsonProperty("CompanyCode")
	private String companyCode;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCheckpoint() {
		return checkpoint;
	}
	public void setCheckpoint(String checkpoint) {
		this.checkpoint = checkpoint;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getTimeStamp() {
		return timeStamp;
	}
	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}
	public String getNextCheckpoint() {
		return nextCheckpoint;
	}
	public void setNextCheckpoint(String nextCheckpoint) {
		this.nextCheckpoint = nextCheckpoint;
	}
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyCode() {
		return companyCode;
	}
	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}
	
}

package com.hoau.wechat.vo;

/** 
* @ClassName: Vouchers 
* @Description: 奖券实体
* @author xujun jun.xu@hoau.net
* @date 2014年7月30日 下午7:18:54 
*  
*/
public class Vouchers {
	// 唯一标示
	private int id;
	// 奖券名称
	private String vouchersName;
	// 奖券状态（0 未使用   1已使用）
	private int status;
	// 抵用券编码
	private String vouchersCode;
	// 奖券类型
	private String type;
	// 有效天数
	private int expiry;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getExpiry() {
		return expiry;
	}
	public void setExpiry(int expiry) {
		this.expiry = expiry;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getVouchersName() {
		return vouchersName;
	}
	public void setVouchersName(String vouchersName) {
		this.vouchersName = vouchersName;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getVouchersCode() {
		return vouchersCode;
	}
	public void setVouchersCode(String vouchersCode) {
		this.vouchersCode = vouchersCode;
	}
	
}

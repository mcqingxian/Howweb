package com.hoau.wechat.vo;

import java.util.Date;

/**
* @ClassName: CompusActivityInfo
* @Description:参加活动信息
* @author hairen.long@hoau.net
* @date 2015年5月20日 下午9:57:29
*/
public class CompusActivityInfo {
	private String id;
	private String openid;
	private String competitionCode;//参赛编号
	private int forwardCount;//好友转发数
	private int status;//是否抽过奖  0:未抽  1：抽过
	private String friendOpenid;
	private String nickname;
	private String country;
	private String province;
	private String city;
	private Date createTime;
	
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getFriendOpenid() {
		return friendOpenid;
	}
	public void setFriendOpenid(String friendOpenid) {
		this.friendOpenid = friendOpenid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public int getForwardCount() {
		return forwardCount;
	}
	public void setForwardCount(int forwardCount) {
		this.forwardCount = forwardCount;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCompetitionCode() {
		return competitionCode;
	}
	public void setCompetitionCode(String competitionCode) {
		this.competitionCode = competitionCode;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
}

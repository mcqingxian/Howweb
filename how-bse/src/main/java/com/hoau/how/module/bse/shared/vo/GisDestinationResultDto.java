package com.hoau.how.module.bse.shared.vo;

import java.io.Serializable;
import java.util.List;

/**
 * @author 唐征征
 * @date 2017/7/27 上午8:25
 * @description GIS目的站信息dto
 */
public class GisDestinationResultDto implements Serializable {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;
	
	/**
	 * 地址纬度
	 */
	private String lat;
	/**
	 * 地址经度
	 */
	private String lng;
	/**
	 * 目的站集合
	 */
	private List<GisOrgDto> result;
	/**
	 * 异常消息
	 */
	private List<GisExceptionMsgDto> params;
	/**
	 * 状态
	 */
	private int status;
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public List<GisOrgDto> getResult() {
		return result;
	}
	public void setResult(List<GisOrgDto> result) {
		this.result = result;
	}
	public List<GisExceptionMsgDto> getParams() {
		return params;
	}
	public void setParams(List<GisExceptionMsgDto> params) {
		this.params = params;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
